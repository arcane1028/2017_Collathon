public class PhraseItem {

    private String phrase;
    private String time;

    public PhraseItem(){
        this.phrase = phrase;
        this.time = time;
    }

    public String getPhrase() {
        return phrase;
    }

    public void setPhrase(String phrase) {
        this.phrase = phrase;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
